class User {
  final String id;
  final String name;
  final String email;
  final String avatarUrl;
  final String cep;
  final String rua;
  final String cidade;
  final String estado;


  const User({
    required this.id,
    required this.name,
    required this.email,
    required this.avatarUrl,
    required this.cep,
    required this.rua,
    required this.cidade,
    required this.estado,
  });
}
